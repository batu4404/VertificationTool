package core.cfg.declaration;


/**
 * @author lenovo
 * kêt thúc của câu lệnh điều kiện
 * chưa thông tin về node tiếp sau của câu lênh rẽ nhánh (next)
 * và 2 con trỏ tới 2 node cuối cùng của 2 nhánh then, else
 */
public class EndCondition extends EndNode {
	private CFGNode endOfThenBranch;	// trỏ tới node cuối cùng của nhánh then
	private CFGNode endOfElseBranch;	// trỏ tới node cuối cùng của nhánh else
	
	
	public CFGNode getEndOfThenBranch() {
		return endOfThenBranch;
	}
	public void setEndOfThenBranch(CFGNode endOfThenBranch) {
		this.endOfThenBranch = endOfThenBranch;
	}
	public CFGNode getEndOfElseBranch() {
		return endOfElseBranch;
	}
	public void setEndOfElseBranch(CFGNode endOfElseBranch) {
		this.endOfElseBranch = endOfElseBranch;
	}
	
}
